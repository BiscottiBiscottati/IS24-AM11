package it.polimi.ingsw.am11.model.players.utils;

/**
 * Enumeration for the color of a player.
 * <p>
 * This enumeration represents the possible colors a player can have in the game. The colors are
 * <code>RED</code>, <code>BLUE</code>, <code>GREEN</code>, and <code>YELLOW</code>.
 */
public enum PlayerColor {
    RED,
    BLUE,
    GREEN,
    YELLOW
}
